     * @return \Illuminate\Http\Response
     */
    public function update(University $university)
    {
        $university->update(
            request()->validate([
                'name' => ['required','max:255'],
                'email' => ['email', 'max:75'],
                'seats' => ['digits_between:1,10240'],
                'established' => ['date'],
            ])
            );
            return redirect()->route('dashboard')
                ->with(['toast' => ['message' => 'University updated successfully']]);
    }

